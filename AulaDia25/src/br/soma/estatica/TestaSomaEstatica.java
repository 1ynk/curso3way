package br.soma.estatica;

public class TestaSomaEstatica {
	
	
	public static void somaNumero(double x, double y){
		
		double res = x + y;
		System.out.println("valor da soma: "+ res);
		
	}
	
	public static void multiplicaNumero(double x, double y) {
		double res = x * y;
		System.out.println("resultado da multiplicação: "+res);
	}

}
